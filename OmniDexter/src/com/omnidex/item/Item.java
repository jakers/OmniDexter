    public static Item findItemByName(String name) {
    	Item item = null;
    	for (Item i : Item.values()) {
    		if (i.toString().toLowerCase().equals(name.toLowerCase())) {
    			item = i;   			
    			break;
    		}
    	}
    	return item;
    }
    
